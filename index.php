                    <h3 class="section-subheading text-muted">En los dos últimos años, cinco hospitales realizaron compras de medicamentos a través de trato directo, un tipo de compra excepcional que se transformó en una regla general, según revelan varios informes de la Contraloría General de la República. Los hospitales pagan hasta un 300% más por los mismos fármacos que vende Cenabast, lo que genera desabastecimiento de otros medicamentos, aumento de la deuda hospitaralia y obligando a los pacientes a adquirirlos por su propia cuenta.</h3>
                    <p class="reportaje"> De cada 100 pesos que debe el Estado en Salud, 83 corresponden a la compra de medicamentos. Así de lapidaria fue la conclusión del <a href="http://www.dipres.gob.cl/572/articles-154339_doc_pdf.pdf">informe que elaboró la Dirección de Presupuestos (Dipres)</a> sobre la deuda hospitalaria que tiene el sistema de salud público chileno. En el documento, publicado a finales del año 2016, la Dipres explicaba que el deuda había superado los $255 mil millones de pesos, lo que equivale a construir casi dos hospitales de última generación. “Se tiene poca información respecto a la gestión de medicamentos dentro de la red pública”, indicaba la entidad en el informe.</p>
                    <p class="reportaje">En el mismo informe la Dipres considera a la deuda hospitalaria como “un problema crítico cuando alcanza niveles que comprometen la operación normal, tanto de los establecimientos de salud como de los mismos Servicios de Salud” y agregaba que “los Servicios con mayor nivel de deuda son los de la Región Metropolitana, situación que no ha variado en los últimos años”. </p>
                    <p class="reportaje">El 20 de marzo, Jorge Bermúdez, contralor general de la República, señalaba en la comisión investigadora de compras de Fonasa, que el resultado de los diversos sumarios y auditorías realizadas a distintos organismos de salud entre 2015 y 2016 había arrojado una serie de infracciones en la norma de compras públicas. “El trato directo es muy recurrente. Nosotros acabamos de ver, por ejemplo, que la justificación para comprar un espectrómetro -no sé qué será eso- que nos dio un hospital para hacer el trato directo fue que se tomaba la muestra o la foto -no sé, disculpen la ignorancia- en cinco segundos, y luego otro hospital nos dio la justificación para utilizar el trato directo que aquello se llevaba a cabo en 0,5 segundos, y que todos los otros lo hacían en cinco segundos”, ejemplificaba el contralor.</p>
                    <p class="reportaje">Una simple búsqueda en la página web de la Contraloría permite encontrar diversos sumarios realizados a distintos hospitales públicos de la comuna de Santiago en donde se da cuenta, entre otras irregularidades, de un alto porcentaje de compras de medicamentos realizadas a través de Trato Directo, vale decir, un contrato realizado a un solo proveedor, previa resolución fundada y siendo siempre un mecanismo de compra excepcional, según indica la Ley de Compras n°19.886. Se agrega en los informes de Contraloría que de haber realizado varias de las compras a Cenabast, se habría efectuado un ahorro de millones de pesos.</p>
                    <p class="reportaje">El trato directo, de acuerdo a la ley de compras, tiene distintas causales: una se produce cuando fracasa el convenio marco, fracasa la licitación y la licitación privada; la otra se da cuando el proveedor es exclusivo. ¿Cuándo el proveedor es exclusivo? Cuando el hospital establece requisitos para adjudicar una compra que se pueda asegurar a través de un certificado de confianza u otro instrumento apelando a que solamente un laboratorio posee características especiales. Gran parte de los oficios que se da en los hospitales chilenos está dado porque se compra argumentando que el proveedor es exclusivo a través de la confianza que se genera entre el médico y el laboratorio.</p>
